export const AU_TO_METERS = 1.496e11;
export const G_TO_MS2 = 9.80665;
export const SEC_TO_DAYS = 1 / 86400;
export const MS_TO_KMS = 1 / 1000;

export const auToMeters = au => au * AU_TO_METERS;
export const gToMs2 = g => g * G_TO_MS2;
export const secToDays = s => s * SEC_TO_DAYS;
export const msToKms = v => v * MS_TO_KMS;
